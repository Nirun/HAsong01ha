<div class="tab">คุณสมบัติของผู้เข้าอบรม </div><br />

หัวหน้างานหรือผู้ช่วยในหน่วยสนับสนุนบริการ เช่น ฝ่ายบริหารงานทั่วไป งานบริหารงานบุคคล/งานการเจ้าหน้าที่ งานพัสดุ งานธุรการ งานการเงิน งานโภชนาการ งานรักษาความปลอดภัย งานยานพาหนะ งานซักฟอก ศูนย์จ่ายกลาง งานซ่อมบำรุง เป็นต้น หรือผู้ที่สนใจการพัฒนาคุณภาพด้านสนับสนุนบริการ 
<br /><br />

<div class="tab">วัตถุประสงค์ เพื่อให้ผู้เข้าอบรม</div>
 <br />
1. เข้าใจและอธิบายแนวคิด/ปรัชญาของกระบวนการพัฒนาคุณภาพ <br />สามารถวิเคราะห์หน้าที่/เป้าหมายติดตามและประเมินผลการพัฒนาคุณภาพหรือประเมินการบรรลุเป้าหมายของหน่วยงานได้อย่างเหมาะสม รวมทั้งสามารถประยุกต์และเชื่อมโยงกิจกรรมการพัฒนาคุณภาพกับงานประจำได้ 
<br />
2. มีความสามารถประเมินตนเองตามมาตรฐาน เพื่อค้นหาโอกาสพัฒนา บันทึกความก้าวหน้า และประเมินการเปลี่ยนแปลงที่เกิดขึ้น 
<br />
3. ได้เรียนรู้และทราบแนวทางการนำมาตรฐานการมุ่งเน้นทรัพยากรบุคคล สิ่งแวดล้อมในการดูแลผู้ป่วย การประเมินด้านสิ่งแวดล้อมและความปลอดภัยในระดับหน่วยงาน/ระดับโรงพยาบาล และสามารถนำไปประยุกต์ใช้ได้อย่างเหมาะสม 

<br />
<br />
<div class="tab">เนื้อหา</div><br />
1. แนวคิดและปรัชญาของการพัฒนาคุณภาพโรงพยาบาล การแปลความหมายของ HA ในมุมมองของงานสนับสนุนบริการ 
<br />
2. เรียนรู้ความมุ่งหมาย ประเด็นสำคัญของมาตรฐานการมุ่งเน้นทรัพยากรบุคคล สิ่งแวดล้อมในการดูแลผู้ป่วย การประเมินด้านสิ่งแวดล้อมและความปลอดภัย รวมทั้งเรียนรู้วิธีการใช้ HA Scorebook เพื่อประเมินระดับการปฏิบัติตามมาตรฐาน 
<br />
3. การวิเคราะห์หน่วยงานอย่างเป็นระบบ เชื่อมโยงกับกิจกรรมหัวหน้าพาทำคุณภาพ การทบทวนเหตุการณ์สำคัญต่างๆ การวางระบบป้องกันโดยใช้เครื่องมือคุณภาพที่เหมาะสม เช่น root cause analysis เป็นต้น 
<br />
4. ประเด็นสำคัญในการพัฒนาหน่วยงานสนับสนุนบริการจากประสบการณ์การเยี่ยมสำรวจ 
<br />
5. การบริหารความเสี่ยง เครื่องชี้วัดคุณภาพ การประกันคุณภาพ และการพัฒนาคุณภาพอย่างต่อเนื่องแบบเรียบง่าย 
<br />
6. การใช้ประโยชน์จาก Service Profile เพื่อวิเคราะห์บริบทของตนเอง กำหนดเป้าหมาย วางแผน ตรวจสอบความเชื่อมโยง ติดตามและบันทึกความก้าวหน้า 



